prs.slide_height = SLIDE_H
BLANK = prs.slide_layouts[6]


def add_slide(bg=WHITE):
    slide = prs.slides.add_slide(BLANK)
    fill = slide.background.fill
    fill.solid()
    fill.fore_color.rgb = bg
    return slide


def add_text(slide, left, top, width, height, runs, align=PP_ALIGN.LEFT):
    """runs: список абзацев; абзац = список (text, size, bold, color)."""
    box = slide.shapes.add_textbox(left, top, width, height)
    tf = box.text_frame
    tf.word_wrap = True
    for i, para in enumerate(runs):
        p = tf.paragraphs[0] if i == 0 else tf.add_paragraph()
        p.alignment = align
        p.space_after = Pt(6)
        for text, size, bold, color in para:
            r = p.add_run()
            r.text = text
            r.font.size = Pt(size)
            r.font.bold = bold
            r.font.color.rgb = color
    return box


def add_accent_bar(slide, color=RED, left=Inches(0.6), top=Inches(1.28), width=Inches(1.1)):
    shape = slide.shapes.add_shape(1, left, top, width, Pt(4))  # 1 = rectangle
    shape.fill.solid()
    shape.fill.fore_color.rgb = color
    shape.line.fill.background()
    return shape


def bullets(items, size=15, color=NAVY):
    """items: список строк или (строка, bold-префикс)."""
    out = []
    for it in items:
        if isinstance(it, tuple):
            head, rest = it
            out.append([("•  ", size, False, RED), (head, size, True, color), (rest, size, False, GRAY)])
        else:
            out.append([("•  ", size, False, RED), (it, size, False, color)])
    return out


def screenshot_slide(title, subtitle, points, image, note=None):
    """Слайд: текст слева, скриншот справа."""
    slide = add_slide()
    add_text(slide, Inches(0.6), Inches(0.4), Inches(5.4), Inches(0.9),
             [[(title, 26, True, NAVY)]])
    add_accent_bar(slide)
    add_text(slide, Inches(0.6), Inches(1.5), Inches(5.0), Inches(0.7),
             [[(subtitle, 14, True, BLUE)]])
    add_text(slide, Inches(0.6), Inches(2.2), Inches(5.0), Inches(4.6),
             bullets(points))
    if note:
        add_text(slide, Inches(0.6), Inches(6.7), Inches(5.0), Inches(0.6),
                 [[(note, 11, False, GRAY)]])
    img_path = ASSETS / image
    pic = slide.shapes.add_picture(str(img_path), Inches(5.9), Inches(0.75), width=Inches(7.0))
    # рамка
    pic.line.color.rgb = RGBColor(0xD0, 0xD5, 0xDE)
    pic.line.width = Pt(1)
    return slide


