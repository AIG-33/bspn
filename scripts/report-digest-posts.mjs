// ---------- дизайн сообщения ----------

const DIVIDER = "━━━━━━━━━━━━━━━━━━";
const THIN = "· · · · · · · · · · · · · · ·";

function esc(s) {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

function buildPost(event) {
  return [
    `🏛 <b>БСПН · НОВОСТНОЙ КАНАЛ</b>`,
    `<i>📅 ${esc(event.dateLabel)}</i>`,
    "",
    DIVIDER,
    `<b>${esc(event.category)}</b>`,
    DIVIDER,
    "",
    `✦ <b>${esc(event.title)}</b>`,
    "",
    `<blockquote>${esc(event.lead)}</blockquote>`,
    "",
    `▫️ <b>Ключевое</b>`,
    ...event.points.map((p) => ` •  ${esc(p)}`),
    "",
    THIN,
    "",
    `✅ <b>Результат</b>`,
    `${esc(event.result)}`,
    "",
    DIVIDER,
    `💼 <i>БСПН им. Кунявского — голос бизнеса с 1990 года</i>`,
    `<i>94 компании · 7 ассоциаций · 20+ советов при госорганах</i>`,
  ].join("\n");
}

const KEYBOARD = {
  inline_keyboard: [
    [{ text: "🚀 Вступить в союз", url: "https://bspn.by/membership/join" }],
    [
      { text: "🌐 Сайт союза", url: "https://bspn.by" },
      { text: "✉️ Написать нам", url: "https://bspn.by/contacts" },
    ],
  ],
};

// ---------- отправка ----------

async function tg(method, params) {
  const res = await fetch(`${API}/${method}`, {
    method: "POST",
    headers: { "content-type": "application/json" },
    body: JSON.stringify(params),
  });
  const d = await res.json();
  if (!d.ok) throw new Error(`${method}: ${d.description}`);
  return d.result;
}

async function post(n, target) {
  const event = EVENTS[n - 1];
  if (!event) throw new Error(`Нет события №${n} (всего ${EVENTS.length})`);
  const params = {
    chat_id: target === "group" ? GROUP_ID : CHANNEL,
    text: buildPost(event),
    parse_mode: "HTML",
    reply_markup: KEYBOARD,
    link_preview_options: { is_disabled: true },
  };
  if (target === "group") params.message_thread_id = GROUP_NEWS_TOPIC_ID;
  await tg("sendMessage", params);
  console.log(`✅ Опубликован пост №${n}: ${event.dateLabel} — ${event.title}`);
}

// ---------- CLI ----------

const args = process.argv.slice(2);
const target = args.includes("--to") && args[args.indexOf("--to") + 1] === "group" ? "group" : "channel";

if (args.includes("--list")) {
  EVENTS.forEach((e, i) => console.log(`${String(i + 1).padStart(2)}. ${e.dateLabel} — ${e.title}`));
} else if (args.includes("--preview")) {
  const n = Number(args[args.indexOf("--preview") + 1]) || 1;
  console.log(buildPost(EVENTS[n - 1]));
} else if (args.includes("--post")) {
  const n = Number(args[args.indexOf("--post") + 1]) || 1;
  await post(n, target);
} else if (args.includes("--post-all")) {
  const from = args.includes("--from") ? Number(args[args.indexOf("--from") + 1]) : 1;
  for (let i = from; i <= EVENTS.length; i++) {
    await post(i, target);
    // Пауза, чтобы не упереться в лимиты Telegram и сохранить порядок
    if (i < EVENTS.length) await new Promise((r) => setTimeout(r, 3500));
  }
} else {
  console.log("Флаги: --list | --preview N | --post N | --post-all [--from N] [--to group]");
}
